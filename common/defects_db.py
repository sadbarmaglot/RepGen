# Индекс для быстрого поиска по коду
_DEFECTS_BY_CODE = None

def _build_code_index():
    """Построение индекса дефектов по коду"""
    global _DEFECTS_BY_CODE
    if _DEFECTS_BY_CODE is not None:
        return _DEFECTS_BY_CODE

    _DEFECTS_BY_CODE = {}

    for construction_type, defects in DEFECTS_DB.items():
        for defect in defects:
            code = defect.get("code")
            if code:
                _DEFECTS_BY_CODE[code] = {
                    **defect,
                    "construction_type": construction_type
                }

            # Индексируем варианты параметров
            if "parameters" in defect:
                for param_name, param_config in defect["parameters"].items():
                    if param_config.get("type") == "range" and "ranges" in param_config:
                        for range_item in param_config["ranges"]:
                            variant_code = range_item.get("code")
                            if variant_code:
                                _DEFECTS_BY_CODE[variant_code] = {
                                    "code": variant_code,
                                    "parent_code": code,
                                    "construction_type": construction_type,
                                    "material": defect.get("material", ""),
                                    "damage_type": defect.get("damage_type", ""),
                                    "category": range_item.get("category", defect.get("category", "")),
                                    "description": range_item.get("description", ""),
                                    "recommendation": range_item.get("recommendation", defect.get("recommendation", "")),
                                    "parameter": param_name,
                                    "min": range_item.get("min"),
                                    "max": range_item.get("max"),
                                }

    return _DEFECTS_BY_CODE


def get_defect_by_code(code: str) -> dict | None:
    """
    Получить дефект по коду.

    Args:
        code: Код дефекта (например, "STN008_1")

    Returns:
        Словарь с данными дефекта или None, если не найден
    """
    index = _build_code_index()
    return index.get(code)


def get_all_codes() -> list[str]:
    """Получить список всех кодов дефектов"""
    index = _build_code_index()
    return list(index.keys())


def get_defects_text(construction_type: str = None) -> str:
    """
    Получить текст со всеми дефектами для промпта.
    Теперь включает коды дефектов.
    """
    defects_list = []
    if construction_type and construction_type in DEFECTS_DB:
        defects_list = DEFECTS_DB[construction_type]
    else:
        for value in DEFECTS_DB.values():
            if isinstance(value, list):
                defects_list.extend(value)
            elif isinstance(value, dict):
                defects_list.append(value)

    result_lines = []

    for defect in defects_list:
        code = defect.get("code", "")

        # Если у дефекта есть параметры с вариантами, выводим варианты
        if "parameters" in defect:
            for param_name, param_config in defect["parameters"].items():
                if param_config.get("type") == "range" and "ranges" in param_config:
                    unit = param_config.get("unit", "")
                    for range_item in param_config["ranges"]:
                        variant_code = range_item.get("code", "")
                        if variant_code:
                            range_str = ""
                            r_min = range_item.get("min")
                            r_max = range_item.get("max")
                            if r_min is not None and r_max is not None:
                                range_str = f" [{param_name}: {r_min}-{r_max} {unit}]"
                            elif r_max is not None:
                                range_str = f" [{param_name}: до {r_max} {unit}]"
                            elif r_min is not None:
                                range_str = f" [{param_name}: более {r_min} {unit}]"
                            line = f"code: {variant_code};{range_str} description: {range_item.get('description', '')}; recommendation: {range_item.get('recommendation', '')}; category: {range_item.get('category', '')}; material: {defect.get('material', '')}."
                            result_lines.append(line)
        else:
            # Дефект без параметров
            line = f"code: {code}; description: {defect['description']}; recommendation: {defect['recommendation']}; category: {defect['category']}; material: {defect.get('material', '')}."
            result_lines.append(line)

    return "\n".join(result_lines)


