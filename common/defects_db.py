def _tags_str(defect):
    tags = defect.get("tags") or defect.get("tags_list") or []
    return ", ".join(tags) if isinstance(tags, (list, tuple)) else str(tags)


def _visual_str(defect):
    return defect.get("visual_signs") or defect.get("damage_type") or ""


def get_defects_text(construction_type=None):
    """
    Текст дефектов для промпта нейросети.
    Включает код, название, описание, ВИЗУАЛЬНЫЕ ПРИЗНАКИ и ТЕГИ, рекомендацию,
    категорию, материал. Для дефектов с параметрами выводятся варианты диапазонов.
    """
    if construction_type:
        defects_list = get_defects_for_type(construction_type)
    else:
        defects_list = list(ALL_DEFECTS)

    lines = []
    for defect in defects_list:
        title = defect.get("title", "")
        visual = _visual_str(defect)
        tags = _tags_str(defect)
        material = defect.get("material", "")
        params = defect.get("parameters")
        if params:
            for param_name, cfg in params.items():
                unit = cfg.get("unit", "")
                for r in cfg.get("ranges", []):
                    vcode = r.get("code", "")
                    if not vcode:
                        continue
                    rng = ""
                    rmin, rmax = r.get("min"), r.get("max")
                    if rmin is not None and rmax is not None:
                        rng = f" [{param_name}: {rmin}-{rmax} {unit}]"
                    elif rmax is not None:
                        rng = f" [{param_name}: до {rmax} {unit}]"
                    elif rmin is not None:
                        rng = f" [{param_name}: более {rmin} {unit}]"
                    lines.append(
                        f"code: {vcode}; title: {title};{rng} description: {r.get('description','')}; "
                        f"visual_signs: {visual}; tags: {tags}; recommendation: {r.get('recommendation','')}; "
                        f"category: {r.get('category','')}; material: {material}."
                    )
        else:
            lines.append(
                f"code: {defect.get('code','')}; title: {title}; description: {defect.get('description','')}; "
                f"visual_signs: {visual}; tags: {tags}; recommendation: {defect.get('recommendation','')}; "
                f"category: {defect.get('category','')}; material: {material}."
            )
    return "\n".join(lines)


