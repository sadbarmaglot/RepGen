_DEFECTS_BY_CODE = None


def _first_type_name(defect):
    for tid in defect.get("applies_to", []):
        t = _TYPE_BY_ID.get(tid)
        if t:
            return t["name"]
    return ""


def _build_code_index():
    global _DEFECTS_BY_CODE
    if _DEFECTS_BY_CODE is not None:
        return _DEFECTS_BY_CODE
    index = {}
    for defect in ALL_DEFECTS:
        code = defect.get("code")
        if not code:
            continue
        ctype = _first_type_name(defect)
        params = defect.get("parameters")
        index_entry = {**defect, "construction_type": ctype}
        if params:
            first_range = next(
                (
                    r
                    for cfg in params.values()
                    for r in cfg.get("ranges", [])
                    if r.get("code")
                ),
                None,
            )
            if first_range:
                index_entry["default_variant_code"] = first_range.get("code", "")
                if not index_entry.get("category"):
                    index_entry["category"] = first_range.get("category", "")
                if not index_entry.get("recommendation"):
                    index_entry["recommendation"] = first_range.get("recommendation", "")
        index[code] = index_entry
        if params:
            for param_name, cfg in params.items():
                for r in cfg.get("ranges", []):
                    vcode = r.get("code")
                    if not vcode:
                        continue
                    index[vcode] = {
                        "code": vcode,
                        "parent_code": code,
                        "title": defect.get("title", ""),
                        "construction_type": ctype,
                        "applies_to": defect.get("applies_to", []),
                        "material": defect.get("material", ""),
                        "visual_signs": defect.get("visual_signs", defect.get("damage_type", "")),
                        "tags": defect.get("tags", defect.get("tags_list", [])),
                        "category": r.get("category", defect.get("category", "")),
                        "description": r.get("description", ""),
                        "recommendation": r.get("recommendation", defect.get("recommendation", "")),
                        "parameter": param_name,
                        "unit": cfg.get("unit", ""),
                        "min": r.get("min"),
                        "max": r.get("max"),
                    }
    # Алиасы legacy-кодов
    for legacy, canonical in LEGACY_CODE_ALIASES.items():
        if canonical in index and legacy not in index:
            index[legacy] = {
                **index[canonical],
                **LEGACY_CODE_FIELD_OVERRIDES.get(legacy, {}),
                "alias_of": canonical,
                "legacy_code": legacy,
            }
    _DEFECTS_BY_CODE = index
    return index


def get_defect_by_code(code):
    """Получить дефект по коду (включая варианты параметров и legacy-алиасы)."""
    return _build_code_index().get(code)


